void	putbits(char src)
{
	size_t	i;

	i = 0;
	while (i < 8)
	{
		if (src & (1 << (8 - i - 1)))
			ft_putnbr(1);
		else
			ft_putnbr(0);
		i++;
	}
	ft_putchar('|');
}


void function(char *a)
{
	for (size_t i = 0; i < 8; i++)
		putbits(a[i]);
	ft_putendl("");
}

